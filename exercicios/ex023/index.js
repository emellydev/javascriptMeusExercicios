// Switch/Case

// checagem com uma variável
let variavel = 7

switch (variavel) {
    case 0:
        variavel = 'null';
        break
    case 1:
        variavel = 'caso 1';
        break
    case 2: 
        variavel = 'caso 2';
        break
    case 3:
        variavel = 'caso 3';
        break
    case 4: 
        variavel = 'caso 4';
        break
    case 5:
        variavel = 'caso 5';
        break
    case 6:
        variavel = 'caso 6';
        break
    default : // similar ao else
        variavel = ''
} 
console.log(variavel)
